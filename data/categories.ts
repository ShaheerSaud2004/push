export const getCategoryById = (id: string, customCategories: Category[] = []): Category | undefined => {
  return [...defaultCategories, ...customCategories].find(cat => cat.id === id);
};

export const getAllWords = (categoryIds: string[], customCategories: Category[] = []): string[] => {
  const words: string[] = [];
  categoryIds.forEach(id => {
    const category = getCategoryById(id, customCategories);
    if (category) {
      words.push(...category.words);
    }
  });
  return words;
};
